import { API_BASE_URL } from './config';
import { fetchWithTimeout } from './fetchWithTimeout';


export const fetchNearbyRoutes = async (lat, lng) => {
  const response = await fetchWithTimeout(`${API_BASE_URL}/routes/nearby?lat=${lat}&lng=${lng}`);
  if (!response.ok) {
    throw new Error(`HTTP error! status: ${response.status}`);
  }
  return await response.json();
};


export const buildRoute = async ({ from, to, stops }) => {
  const response = await fetchWithTimeout(`${API_BASE_URL}/routes/build`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        start: from,
        end: to,
        waypoints: stops,
      }),
    });
  if (!response.ok) {
    throw new Error(`HTTP error! status: ${response.status}`);
  }
  return await response.json();
};



